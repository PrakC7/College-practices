import math
#ADDITION FUNCTION.
def add(a,b):
    return a+b
#SUBTRACTION FUNCTION.
def sub(a,b):
    return a-b
#MULTIPLICATION FUNCTION.
def mul(a,b):
    return a*b
#DIVISION FUNCTION.
def div(a,b):
    return a/b
#FACTORIAL FUNCTION.
def fact(a):
    return math.factorial(a)
#SQUARE ROOT FUNCTION.
def sqrt(a):
    return math.sqrt(a)
